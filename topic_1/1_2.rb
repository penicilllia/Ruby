# Площадь треугольника

print 'Введите длину основания треугольника: '
a = gets.to_i
print 'Введите длину высоты треугольника: '
h = gets.to_i

if a == 0 || h == 0
  puts 'Основание или высота треугольника не могут быть нулевыми.'
elsif a < 0 || h < 0
  puts 'Основание или высота треугольника не могут быть отрицательными числами.'
else
  print "Площадь треугольника #{0.5 * a * h}."
end
